# <탈출문자>

print("백문이 불여일견 백견이 불여일타")

# \n : 줄바꿈
print("백문이 불여일견\n백견이 불여일타")

# \" , \' : 따옴표 출력
print("사과는 '애플'이다.")
print('사과는 "애플"이다.')
print("사과는 \"애플\"이다.")
print("사과는 \'애플\'이다.")

# \\ : \ 출력
print("현재 경로: C:\\Users\\")

# \r : 커서를 맨 앞으로 이동
print("Red Apple\rPine")

# \b : 백스페이스 (한 글자 삭제)
print("Redd\bApple")

# \t : 탭 (일정 간격 띄어쓰기)
print("Red\tApple")