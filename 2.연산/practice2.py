# <간단한 수식>

print( 2 + 3 * 4)
print( (2+3) * 4)

number = 2 + 3 * 4
print(number)

number += 2 # number = number + 2 와 같은 표현
print(number)

number -= 2 # number = number - 2 와 같은 표현
print(number)

number *= 2 # number = number * 2 와 같은 표현
print(number)

number /= 2 # number = number / 2 와 같은 표현
print(number)

number %= 5 # number = number % 5 와 같은 표현
print(number)