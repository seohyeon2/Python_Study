# 덧셈, 뺄셈, 곱셈, 나눗셈 가능
print(1+1)
print(5-2)
print(2*2)
print(10/2)

# 제곱, 나머지, 몫 가능
print(2**3)
print(5%3)
print(10//3)

# 비교연산도 가능
print(10 > 3)
print(10 < 3)
print(4 >= 7)
print(4 <= 7)
print(3 == 3)
print(4 == 3)
print(3+7 == 10)
print(1 != 3)
print(not (1 == 3))

# and 조건 : 둘 다 참이여야 참
print((3>0) and (3<5)) 
print((3>0) & (3<5)) 

# or 조건 : 둘 중에 하나만 참이여도 참
print((3>0) or (3>5)) 
print((3>0) | (3>5)) 

print(5 > 4 > 3)
print(5 > 4 > 7)